package code;
public class Operator {
    public static void main(String[] args){
        System.out.println("5+5="+5+5);

        int i = 100;
        int j = 9;
        System.out.println(i/j);

        int k = 6;
        int m = 4;
        System.out.println(k%m);

        int a = 3;
        int b = 4;

        a++;
        ++b;
        System.out.println(a);
        System.out.println(b);
    }
}
